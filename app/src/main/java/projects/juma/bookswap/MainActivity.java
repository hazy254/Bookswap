    private void getData() {

        //Showing a progress dialog
        final ProgressDialog loading = ProgressDialog.show(this, "Loading Data", "Please wait...", false, false);

        //Creating a json array request
        JsonArrayRequest jsonArrayRequest = new JsonArrayRequest(Config.DATA_URL,
                new Response.Listener<JSONArray>() {
                    @Override
                    public void onResponse(JSONArray response) {
                        //Dismissing progress dialog
                        loading.dismiss();

                        //calling method to parse json array
                        parseData(response);
                    }
                },
                new Response.ErrorListener() {
                    @Override
                    public void onErrorResponse(VolleyError error) {

                    }
                });

        //Creating request queue
        RequestQueue requestQueue = Volley.newRequestQueue(this);

        //Adding request to the queue
        requestQueue.add(jsonArrayRequest);
    }

    private void parseData(JSONArray array) {
        for (int i = 0; i < array.length(); i++) {
            Book current = new Book();
            JSONObject json = null;
            try {
                json = array.getJSONObject(i);
                current.setCoverUrl(json.getString(Config.TAG_COVER));
                current.setAuthor(json.getString(Config.TAG_AUTHOR));
                current.setTitle(json.getString(Config.TAG_TITLE));
                current.setOwner(json.getString(Config.TAG_OWNER));
                current.setSynopsis(json.getString(Config.TAG_SYNOPSIS));
                current.setPhoneNumber(json.getString(Config.TAG_NUMBER));


            } catch (JSONException e) {
                e.printStackTrace();
            }
            bookList.add(current);

        }
        adapter = new BookAdapter(this, bookList);
        adapter.notifyDataSetChanged();
        recyclerView.setAdapter(adapter);

    }

